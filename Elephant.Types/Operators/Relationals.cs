namespace Elephant.Types.Operators
{
	/// <summary>
	/// Relational operators.
	/// </summary>
	public enum Relationals
	{
		/// <summary>
		/// No operator.
		/// </summary>
		None,

		/// <summary>
		/// ==.
		/// </summary>
		Equal,

		/// <summary>
		/// !=.
		/// </summary>
		NotEqual,

		/// <summary>
		/// >.
		/// </summary>
		GreaterThan,

		/// <summary>
		/// <![CDATA[<]]>.
		/// </summary>
		SmallerThan,

		/// <summary>
		/// >=.
		/// </summary>
		GreaterOrEqual,

		/// <summary>
		/// <![CDATA[<=]]>.
		/// </summary>
		SmallerOrEqual,
	}
}
