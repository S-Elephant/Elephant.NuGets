namespace Elephant.Types.Operators
{
    public enum Relationals
    {
        /// <summary>
        /// No operator.
        /// </summary>
        None,

        /// <summary>
        /// ==.
        /// </summary>
        Equal,

        /// <summary>
        /// !=.
        /// </summary>
        NotEqual,

        /// <summary>
        /// >.
        /// </summary>
        GreaterThan,

        /// <summary>
        /// <.
        /// </summary>
        SmallerThan,

        /// <summary>
        /// >=.
        /// </summary>
        GreaterOrEqual,

        /// <summary>
        /// <=.
        /// </summary>
        SmallerOrEqual,
    }
}
